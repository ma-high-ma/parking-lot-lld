class Vehicle:
    def __init__(self, type, regno, color):
        self.type = type
        self.regno = regno
        self.color = color

    def set_type(self, type):
        self.type = type

    def get_type(self):
        return self.type

    def set_regno(self, regno):
        self.regno = regno

    def get_regno(self):
        return self.regno

    def set_color(self, color):
        self.color = color

    def get_color(self):
        return self.color

